/*I/O口引脚设定*/
#define SCL P1_3    //P1_3口模拟时钟口
#define SDA P1_2    //P1_2口模拟数据口
  #define I2C_SDA_OUT()           P1DIR |= 0x04       // P1_2 = 数据SDA，P1_2 输出
  #define I2C_SDA_IN()            P1DIR &= ~0x04      // P1_2 = 数据SDA，P1_2 输入


